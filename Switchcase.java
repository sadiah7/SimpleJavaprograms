class Switchcase
{
  public static void main(String[] args)
  {
  int a;
  a = 5;
  switch(a)
  {
    case 1:
            System.out.println("Case 1");
            break;
    case 2:
            System.out.println("Case 2"); //added by subiya
            break;
    case 3:
            System.out.println("Case 3");
            break;
    default:
            System.out.println("default");
    }
  }
}
